class Calculations(object):
    def __init__(self, num1=0, num2=0):
        self.num1 = num1
        self.num2 = num2

    def addition(self, num1, num2):
        return num1 + num2

    def subtraction(self, num1, num2):
        return num1 - num2

    def division(self, num1, num2):
        return num1 / num2

    def multiplication(self, num1, num2):
        return num1 * num2
